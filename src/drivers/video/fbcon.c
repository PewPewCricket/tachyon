#include <stdint.h>
#include <stddef.h>
#include <lib/string.h>
#include <limine/limine.h>

static unsigned char font[];
static struct limine_framebuffer *_fb;

struct tty_state {
    uint16_t x;
    uint16_t y;
    uint32_t fg_color;
    uint32_t bg_color;
};

static struct tty_state tty_state = {0, 0, 0xFFFFFFFF, 0x00000000};

const uint32_t char_width = 8;
const uint32_t char_height = 16;

static void fbcon_set(const char c, const uint16_t x, const uint16_t y, const uint32_t fg, const uint32_t bg) {
    if (!_fb) return;

    uint32_t *fbptr = (uint32_t*) _fb->address;
    const uint32_t scanline_width = _fb->width;

    for (uint32_t row = 0; row < char_height; row++) {
        for (uint32_t col = 0; col < char_width; col++) {
            const unsigned char bit = (font[c * char_height + row] >> (char_width - 1 - col)) & 1;
            const uint32_t pixel = bit ? fg : bg;
            const uint32_t offset = (y * char_height + row) * scanline_width + (x * char_width + col);

            fbptr[offset] = pixel;
        }
    }
}

void fbcom_scroll() {
    if (!_fb) return;

    const uint32_t bpp = _fb->bpp / 8; // bytes per pixel
    const uint32_t row_bytes = _fb->width * bpp * char_height;
    char *fbptr = (char*) _fb->address;

    // Move all rows up by one character height
    memmove(fbptr,
        fbptr + row_bytes,
        (_fb->height - char_height) * _fb->width * bpp);


    // Clear the last character row
    const uint32_t start = (_fb->height - char_height) * _fb->width * bpp;
    for (uint32_t i = 0; i < row_bytes; i++) {
        fbptr[start + i] = 0;
    }
}

void fbcon_putc(const char c) {
    if (!_fb) return;
    const uint32_t max_x = _fb->width / char_width;
    const uint32_t max_y = _fb->height / char_height;

    switch (c) {
        case '\n':
            tty_state.x = 0;
            tty_state.y++;
            break;
        case '\t':
            tty_state.x = (tty_state.x + 8) & ~7;
            break;
        default:
            fbcon_set(c, tty_state.x, tty_state.y, tty_state.fg_color, tty_state.bg_color);
            tty_state.x++;
    }

    // Handle line wrapping
    if (tty_state.x >= max_x) {
        tty_state.x = 0;
        tty_state.y++;
    }

    // Handle scrolling if we have written past the screen height
    if (tty_state.y >= max_y) {
        fbcom_scroll();
        tty_state.y = max_y - 1;
    }
}

void fbcon_setcol(const uint32_t fg_color, const uint32_t bg_color) {
    tty_state.fg_color = fg_color;
    tty_state.bg_color = bg_color;
}

void fbcon_puts(const char *s) {
    if (!_fb || !s || *s == '\0') return;
    for (size_t i = 0; s[i] != '\0'; i++)
        fbcon_putc(s[i]);
}

void fbcon_setfb(const struct limine_framebuffer *framebuffer) {
    _fb = (struct limine_framebuffer*) framebuffer;
}

